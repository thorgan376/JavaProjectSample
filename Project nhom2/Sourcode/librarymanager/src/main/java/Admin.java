    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        // TODO add your handling code here:
        try{
            long x = Long.parseLong(phone.getText());
            String ename = "";
            ename = name.getText();
            String egender = "";
            if(male.isSelected()){
                egender = "male";
            }if(female.isSelected()){
                egender = "female";
            }
            if("".equals(egender) || "".equals(ename)){
                Component frame = null;
                JOptionPane.showMessageDialog(frame, "Input invalid");
                return;
            }
            String eaddress = address.getText();
            String ephone = phone.getText();
            int etype = parseInt(type.getSelectedItem().toString());
            String eusername = username.getText();
            String epassword = password.getText();
            Employee employee = new Employee(ename, egender, eaddress, ephone, eusername, epassword, etype);
            EmployeeModify.insert(employee);
            showEmployee();
        }catch(Exception ex){
            Component frame = null;
            JOptionPane.showMessageDialog(frame, "Input invalid");
        }
    }//GEN-LAST:event_addActionPerformed

