    private void showEmployee(){
        List<Employee> eList = EmployeeModify.findAll();
        
        tableModel.setRowCount(0);
        eList.forEach(employee -> {
            tableModel.addRow(new Object[] {employee.getEmployeeId(), employee.getName(), employee.getGender(), 
                employee.getAddress(), employee.getPhoneNumber(), employee.getUsername(), employee.getPassword(),
                employee.getAccountProvideType()});
        });
    }
    
