    private void filter(String query){
        TableRowSorter<DefaultTableModel> tr=new TableRowSorter<DefaultTableModel>(tableModel);
        tblBook.setRowSorter(tr);
        if("All".equals(query)){
            tblBook.setRowSorter(tr);
        }else{
            tr.setRowFilter(RowFilter.regexFilter(query,2));
           
        }
        
    }
