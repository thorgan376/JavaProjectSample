    private void addBookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBookActionPerformed
        // TODO add your handling code here:
        try{
            
            String bname = name.getText();
            String btype = type.getSelectedItem().toString();
            String bproducer = producer.getText();
            if("".equals(bname) || "".equals(bproducer)){
                Component frame = null;
                JOptionPane.showMessageDialog(frame, "Input invalid");
                return;
            }
            float bfee = Float.parseFloat(fee.getText());
            int bamount = Integer.parseInt(amount.getText());
            Book book = new Book(bname, btype, bproducer, bfee, bamount);
            BookModify.insert(book);
            Component frame = null;
            JOptionPane.showMessageDialog(frame, "Add Success");
            showBook();
        }catch(Exception ex){
            Component frame = null;
            JOptionPane.showMessageDialog(frame, "Input invalid");
        }
        
    }//GEN-LAST:event_addBookActionPerformed

